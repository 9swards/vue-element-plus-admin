<template>
  <el-sub-menu v-if="haChild" :key="menuList.path" :index="menuList.path">
    <template #title>
      <i :class="menuList.meta.icon || 'location'" />
      <span>{{ menuList.meta.title }}</span>
    </template>
    <el-menu-item-group>
      <menubar-item v-for="v in cMenuList" :key="v.path" :index="v.path" :menu-list="v" />
    </el-menu-item-group>
  </el-sub-menu>

  <el-menu-item v-else :key="menuList.path" :index="menuList.path">
    <el-icon>
      <component :is="menuList.meta.icon || 'setting'" />
    </el-icon>
    <template #title>
      {{ menuList.meta.title }}
    </template>
  </el-menu-item>
</template>

<script lang="ts">
import { defineComponent, computed } from 'vue'
export default defineComponent({
  name: 'MenubarItem',
  props: {
    menuList: {
      type: Object,
      default: () => {
        return {}
      },
    },
  },
  setup(context) {
    const cMenuList = computed(() => context.menuList.children.filter((v) => !v.hidden))
    return {
      haChild: context.menuList.children && context.menuList.children.length > 0,
      cMenuList,
    }
  },
})
</script>
